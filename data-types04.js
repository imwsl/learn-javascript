// 对象
// 对象是JavaScript里面最核心的概念，也是最重要的数据类型
//
// 对象：简单讲，对象是一组“键值对”(key-value)集合
//
// 对象的属性以逗号分隔，最后一个属性可以加逗号(trailing comma)，也可以不加

var obj = {
	foo: 'Hello',
	bar: 'World',
	1: 'a',
	3.2: 3.2,
	1e2: true,
	.234: .234
};
// obj 指向一个对象，对象包含两个键值对(也可以称为“成员”)，键值对以逗号分隔
// 如果键名称是数字，自动转化为字符串 	
// 注意!! 如果键名不符合标识命名条件，且又不是数字就会报错，但加上引号又不报错
//

