// 对象每个键名又可以称为“属性”(property)，键值可以是任何数据类型，如果键值是一个函数
// 我们可以把这个属性叫做"方法"(method)，可以像函数那样调用

var obj3 = {
	p: function(x) {
		return x * x;
	}
};

console.log(obj3.p(9)); // 81

// 属性可以动态创建，如果属性的值是一个对象，我们可以做一个链式引用，如下:

var obj4 = {
	sum: function(x, y) {
		return x + y;
	}
};

obj3.foo = obj4; // 动态创建
console.log(obj3.foo.sum(8, 9)); // 17